package org.opentripplanner.middleware.connecteddataplatform;

/**
 * Used to define the anonymous trip type.
 */
public enum AnonymousTripType {
    /**
     * Rider would like to arrive by the times provided.
     */
    ARRIVE_BY,
    /**
     * Rider would like to depart at the times provided.
     */
    DEPART_AT;
}
